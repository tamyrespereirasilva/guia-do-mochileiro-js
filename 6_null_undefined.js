// principais diferenças entre null e undefined
// null é um valor atribuído a uma variável, indicando que ela não possui valor
// undefined é um valor atribuído a uma variável que não foi inicializada, ou seja, não possui valor.
// exemplo:

let valor = null;
console.log(valor); // null
let valor2;
console.log(valor2); // undefined

// null é um valor atribuído pelo programador, undefined é um valor atribuído pelo JS
