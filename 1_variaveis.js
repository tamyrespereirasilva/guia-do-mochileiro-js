// var, let e const qual usar?

// var: Usado para declarar variáveis com escopo de função ou global. Pode ser redeclarado e atualizado.
// let: Usado para declarar variáveis com escopo de bloco. Pode ser atualizado, mas não redeclarado no mesmo escopo.
// const: Usado para declarar constantes com escopo de bloco. Não pode ser atualizado nem redeclarado.

let umaVariavel; // por padrão é undefined, se você não atribuir um valor a ela
let outraVariavel = 10; 

var a = 1;
let b = 2;
const c = 3;

