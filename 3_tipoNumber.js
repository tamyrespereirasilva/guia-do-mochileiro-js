// Números em JS são todos do tipo Number, não importa se são inteiros ou decimais.
// O tipo Number é um dos tipos primitivos do JavaScript.
// O tipo Number é um tipo primitivo, ou seja, é imutável. Uma vez que um número é criado, ele não pode ser alterado.   

/*
    + (adição)
    - (subtração)
    * (multiplicação)
    / (divisão)
    % (resto da divisão)
    ++ (incremento)
    -- (decremento)
    ** (exponenciação)
    = (atribuição)
    += (atribuição de adição)
    -= (atribuição de subtração)
    *= (atribuição de multiplicação)
    /= (atribuição de divisão)
    %= (atribuição de resto da divisão)
    **= (atribuição de exponenciação)
    == (igualdade)
    === (igualdade estrita)
    != (diferente)
    !== (diferente estrito)
    > (maior que)
    < (menor que)
    >= (maior ou igual que)
*/ 

/*
    Operações
    10 + 5 : 15
    10 - 5 : 5     
    10 * 5 : 50
    10 / 5 : 2
    10 % 5 : 0
    10 ** 5 : 100000
    10++ : 11
    10-- : 9
    10 += 5 : 15
    10 == 5 : false
    10 === 5 : false
    10 != 5 : true
    10 !== 5 : true
    10 > 5 : true
    10 < 5 : false
    10 >= 5 : true
*/

/*
    ParseInt: Converte uma string em um número inteiro.
    parseInt("10") : 10
    parseInt("10.5") : 10       

    ParseFloat: Converte uma string em um número decimal.
    parseFloat("10") : 10   

    ParseString: Converte um número em uma string.
    String(10) : "10"

    TypeOf: Retorna o tipo de dado.
    typeof 10 : "number"
    typeof "10" : "string"
*/